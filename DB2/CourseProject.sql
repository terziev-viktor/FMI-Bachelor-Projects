CREATE FUNCTION DEPTSTAFF (DEPNAME VARCHAR(120))
     RETURNS TABLE (EGN VARCHAR(10),
                    SALARY DOUBLE)
     LANGUAGE SQL
     READS SQL DATA
     NO EXTERNAL ACTION
     DETERMINISTIC
     RETURN
       SELECT EGN, SALARY
         FROM (SELECT * FROM STAFF RIGHT JOIN DEP ON STAFF.DEP = DEP_ID) AS SD
         WHERE SD.NAME = DEPNAME

